let web3;
let contract;
let accounts;
let currentPlayer;

// Initialize Web3 and contract
async function initWeb3() {
  if (typeof window.ethereum !== 'undefined') {
    web3 = new Web3(window.ethereum);
    accounts = await ethereum.request({ method: 'eth_requestAccounts' });
    contract = new web3.eth.Contract(ABI, CONTRACT_ADDRESS);
    document.getElementById("status").textContent = `Status: Connected as ${accounts[0]}`;
    setupBoard();
  } else {
    alert('Please install MetaMask to use this dApp!');
  }
}

// Mint an NFT
async function mintNFT() {
  const tokenURI = document.getElementById("tokenURI").value;
  try {
    await contract.methods.mintNFT(accounts[0], tokenURI).send({ from: accounts[0] });
    alert("NFT Minted Successfully!");
  } catch (error) {
    console.error("Error minting NFT:", error);
  }
}

// Start a new game
async function startGame() {
  const player1 = document.getElementById("player1").value;
  const player2 = document.getElementById("player2").value;
  try {
    await contract.methods.startGame(player1, player2).send({ from: accounts[0] });
    alert("Game Started Successfully!");
    setupBoard();
  } catch (error) {
    console.error("Error starting game:", error);
  }
}

// Make a move on the board
async function makeMove(x, y) {
  try {
    await contract.methods.makeMove(x, y).send({ from: accounts[0] });
    alert("Move made successfully!");
    setupBoard();
  } catch (error) {
    console.error("Error making move:", error);
  }
}

// Setup board
function setupBoard() {
  const boardContainer = document.getElementById("gameBoard");
  boardContainer.innerHTML = ""; // Clear existing board

  for (let x = 0; x < 3; x++) {
    for (let y = 0; y < 3; y++) {
      const cell = document.createElement("div");
      cell.className = "cell";
      cell.addEventListener("click", () => makeMove(x, y));
      boardContainer.appendChild(cell);
    }
  }
}

// Event listeners
document.getElementById("connectWallet").addEventListener("click", initWeb3);
document.getElementById("mintNFT").addEventListener("click", mintNFT);
document.getElementById("startGame").addEventListener("click", startGame);
